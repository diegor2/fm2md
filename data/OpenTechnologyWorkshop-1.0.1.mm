<node CREATED="1403327185342" ID="ID_193445872" MODIFIED="1403451150883" TEXT="Boris Adryan">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      Boris Adryan talked about Low Temperature Monitoring and explained how he became an accidental entrepreneur.
    </p>
    <p>
      
    </p>
    <p>
      Boris runs the Adryan Computational Biology and Genomics Lab, in which there are many specimens that need to be maintained at a temperature below -80 C. He came up with a solution involving chilled Arduinos within the refrigerators, using RF links from Ciseco to report their temperature to a room-temperature Raspberry Pi. The Arduinos cope with the low temperature, but the batteries didn't, until he replaced them with high-endurance versions, NASA approved, which will work for weeks at low temperatures. The batteries are not cheap, but they are readily available&#160;from Argos!
    </p>
    <p>
      
    </p>
    <p>
      Boris has become deeply involved with the Internet of Things, and will be running a NodeRED tutorial at the CamJam on 5th July. There are sill a few places available, so sign up soon!
    </p>
    <p>
      
    </p>
    <p>
